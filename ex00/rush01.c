void	ft_putchar(char c);

void	rush(int x, int y, int i, int j)
{
	i = 1;
	j = 1;
	while (i <= y)
	{
		j = 1;
		while (j <= x)
		{
			if (j == 1 || i == y || i == 1 || j == x)
			{
				if ((i == 1 && j == 1) || (i == y && j == x))
					ft_putchar(47);
				else if ((i == 1 && j == x) || (i == y && j == 1))
					ft_putchar(92);
				else
					ft_putchar(42);
			}
			else
				ft_putchar(' ');
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}
